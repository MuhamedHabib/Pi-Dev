<?php

namespace App\Entity;

use Doctrine\ORM\Mapping as ORM;
use App\Repository\MyformationRespository;
use Symfony\Component\Validator\Constraints as Assert;
use Symfony\Component\Form\Extention\Core\Type\FileType;
/**
 * Myformation
 *
 * @ORM\Table(name="myformation")
 * @ORM\Entity(repositoryClass=MyformationRespository::class)
 */
class Myformation
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer", nullable=false)
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="IDENTITY")
     */
    private $id;

    /**
     * @var string
     * @Assert\NotBlank
     * @ORM\Column(name="libelle", type="string", length=100, nullable=false)
     */
    private $libelle;














    /**
     * @var string
     * @Assert\NotBlank
     * @ORM\Column(name="description", type="string", length=100, nullable=false)
     */
    private $description;

    /**
     * @var string
     *
     * @ORM\Column(name="date", type="string", length=50, nullable=false)
     */
    private $date;

    /**
     * @var string
     *
     * @ORM\Column(name="type", type="string", length=99, nullable=false)
     */
    private $type;

    /**
     * @ORM\Column(type="string")
     * @Assert\NotBlank(message="Upload your image")
     */
    private $image;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getLibelle(): ?string
    {
        return $this->libelle;
    }

    /**
     * @return mixed
     */
    public function getImage()
    {
        return $this->image;
    }

    /**
     * @param mixed $image
     */
    public function setImage($image): void
    {
        $this->image = $image;
    }

    public function setLibelle(string $libelle): self
    {
        $this->libelle = $libelle;

        return $this;
    }

    public function getDescription(): ?string
    {
        return $this->description;
    }

    public function setDescription(string $description): self
    {
        $this->description = $description;

        return $this;
    }

    public function getDate(): ?string
    {
        return $this->date;
    }

    public function setDate(string $date): self
    {
        $this->date = $date;

        return $this;
    }

    public function getType(): ?string
    {
        return $this->type;
    }

    public function setType(string $type): self
    {
        $this->type = $type;

        return $this;
    }






}
